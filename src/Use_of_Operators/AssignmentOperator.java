package Use_of_Operators;

public class AssignmentOperator {
	 public static void main(String[] args) 
	  {
	   
	    int a = 10;
	    int var;

	    //using =
	    var = a;
	    System.out.println("var using =: " + var);

	    //using =+
	    var += a;
	    System.out.println("var using +=: " + var);

	    //using =*
	    var *= a;
	    System.out.println("var using *=: " + var);
	  }

	

}
