package Use_of_Operators;

public class Comparison {
	public static void main(String[] args) {
		 int a = 10, b = 20;

		    // value of a and b
		    System.out.println("a is " + a + " and b is " + b);

		    // == operator
		    System.out.println(a == b);  

		    // != operator
		    System.out.println(a != b);  

		    // > operator
		    System.out.println(a > b);  

		    // < operator
		    System.out.println(a < b);  

		    // >= operator
		    System.out.println(a >= b);  

		    // <= operator
		    System.out.println(a <= b);  
		  }
}
