package Use_of_Operators;

public class Arithmetic {
	

	public static void main(String[] args) {
		int a=5,b=10;
		
		System.out.println("Addition of 20 and 10 is "+(a+b));
		System.out.println("Subtraction of 20 and 10 is "+(a-b));
		System.out.println("Multiplication of 20 and 10 is "+(a*b));
		System.out.println("Division of 20 and 10 is "+(a/b));
	}

}
